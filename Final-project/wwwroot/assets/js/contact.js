$(function () {
    $(document).on('click', '.add-suggestion', function (e) {
        e.preventDefault();
        let userFullName = $(".fullname").val();
        let userEmail = $(".email").val();
        let suggestion = $(".suggestion").val();
        let phoneNumber = $(".phone-number").val();
        let subject = $(".subject").val();

        if (suggestion.trim() == "") {
            toastr["error"]("Suggestion can`t leave empty")
            toastr.options = {
                "closeButton": false,
                "debug": false,
                "newestOnTop": false,
                "progressBar": false,
                "positionClass": "toast-top-center",
                "preventDuplicates": false,
                "onclick": null,
                "showDuration": "300",
                "hideDuration": "1000",
                "timeOut": "5000",
                "extendedTimeOut": "1000",
                "showEasing": "swing",
                "hideEasing": "linear",
                "showMethod": "fadeIn",
                "hideMethod": "fadeOut"
            }
            return;
        }

        $.ajax({
            url: `Contact/AddSuggestion`,
            data: { userFullName, userEmail, suggestion, phoneNumber,subject },
            type: 'POST',
            success: function (response) {
                toastr["success"]("Thanks for your suggestion")
                toastr.options = {
                    "closeButton": false,
                    "debug": false,
                    "newestOnTop": false,
                    "progressBar": false,
                    "positionClass": "toast-top-right",
                    "preventDuplicates": false,
                    "onclick": null,
                    "showDuration": "300",
                    "hideDuration": "1000",
                    "timeOut": "5000",
                    "extendedTimeOut": "1000",
                    "showEasing": "swing",
                    "hideEasing": "linear",
                    "showMethod": "fadeIn",
                    "hideMethod": "fadeOut"
                }

                $(".suggestion").val("");
            },
            error: function (response) {
                toastr["error"]("Login to send suggestion")
                toastr.options = {
                    "closeButton": false,
                    "debug": false,
                    "newestOnTop": false,
                    "progressBar": false,
                    "positionClass": "toast-top-right",
                    "preventDuplicates": false,
                    "onclick": null,
                    "showDuration": "300",
                    "hideDuration": "1000",
                    "timeOut": "5000",
                    "extendedTimeOut": "1000",
                    "showEasing": "swing",
                    "hideEasing": "linear",
                    "showMethod": "fadeIn",
                    "hideMethod": "fadeOut"
                }
            }
        });
    })
})
